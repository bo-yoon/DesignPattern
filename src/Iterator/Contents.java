package Iterator;

public class Contents {
	private String title;
	private int number;
	// 생성자
	public Contents(String title, int num) {
		this.title = title;
		this.number = num;
	}
	
	
	//getter, setter
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
}
